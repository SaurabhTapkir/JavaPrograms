/*
 Unboxing :
 it is process of Converting Non primitive type value back to its primitive datatype
there are two ways to perform unboxing
1.literal way
2. using value() of wrapper class
 -----------------------------------------
                1.literal way
                ---------------
*/
 
class Unboxing {
    public static void main(String[] args) {
    Integer i=120;
    int a=i; // passing non primitive to primitive        
    System.out.println(i.MIN_VALUE); //non primitive
   System.out.println(a);//a.MAX_VALUE 
     
    }
    
}
