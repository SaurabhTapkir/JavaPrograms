/*
 ArrayIndexOutOfBoundsException - occurs when we try
 to access value from given array based on its index value ,
 if index value  is greater or equal to length of given array
  
 */
class AIOBE {
    public static void main(String[] args) {
        int a[] =new int[4];
        System.out.println(a[4]);
    }
    
}
